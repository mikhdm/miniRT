#include "libft.h"

int		ft_isspace(int c)
{
	if (c == '\t' ||
		c == '\n' ||
		c == '\v' ||
		c == '\f' ||
		c == '\r' ||
		c == ' ')
		return (TRUE);
	return (FALSE);
}
